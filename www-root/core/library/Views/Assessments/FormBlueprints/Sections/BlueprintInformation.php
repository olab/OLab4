class Views_Assessments_FormBlueprints_Sections_BlueprintInformation extends Views_HTML {

    protected function validateOptions($options = array()) {
        if (!$this->validateIsSet($options, array("form_type_id", "objectives_title"))) {
            return false;
        }
        if (!$this->validateArray($options, array("form_types", "forms"))) {
            return false;
        }
        return true;
    }

    protected function renderView($options = array()) {
        global $translate;

        $form_types             = $options["form_types"];
        $selected_form_type     = $options["form_type_id"];
        $objectives_title       = $options["objectives_title"];
        $forms                  = $options["forms"];
        $is_published           = Entrada_Utilities::arrayValueOrDefault($options, "published");
        $forms_created          = Entrada_Utilities::arrayValueOrDefault($options, "forms_created");
        $is_publishable         = Entrada_Utilities::arrayValueOrDefault($options, "is_publishable");
        $form_blueprint_id      = Entrada_Utilities::arrayValueOrDefault($options, "form_blueprint_id");
        $blueprint_title        = Entrada_Utilities::arrayValueOrDefault($options, "title");
        $course_related         = Entrada_Utilities::arrayValueOrDefault($options, "course_related");
        $selected_course_id     = Entrada_Utilities::arrayValueOrDefault($options, "course_id");
        $courses_list           = Entrada_Utilities::arrayValueOrDefault($options, "courses_list");
        $blueprint_description  = Entrada_Utilities::arrayValueOrDefault($options, "description");
        $include_instructions   = Entrada_Utilities::arrayValueOrDefault($options, "include_instructions");
        $instructions           = Entrada_Utilities::arrayValueOrDefault($options, "instructions");
        $authors                = Entrada_Utilities::arrayValueOrDefault($options, "authors", array());

        $is_procedure_form = false;
        foreach ($forms as $form_id => $form) {
            if (array_key_exists("item_objectives", $form)) {
                foreach ($form["item_objectives"] as $objective) {
                    if ($objective["objective_code"] == "procedure") {
                        $is_procedure_form = true;
                        break;
                    }
                }
            }
        }
        $publish_class = ($is_publishable && !$is_published) ? "" : " hide";
        ?>
        <?php if ($forms_created) : ?>
            <div class="alert alert-info <?php echo $is_published ? "" : "hide" ?>" id="display-published">
                <ul>
                    <li>
                        <?php echo $translate->_("This template is published."); ?>
                    </li>
                </ul>

            </div>
            <?php if (count($forms) > 0) : ?>
                <strong><?php echo $translate->_("The following forms were generated by this template:"); ?></strong>
                <div id="generated-forms" class="space-above space-below">
                    <table class="table table-bordered table-striped">
                        <thead>
                        <tr>
                            <th><?php echo $translate->_("Form Name"); ?></th>
                            <?php if ($is_procedure_form): ?>
                                <th><?php echo $translate->_("Procedure"); ?></th>
                            <?php endif; ?>
                            <th><?php echo $translate->_("Mapped Objectives"); ?></th>
                            <th><?php echo $translate->_("Date Published"); ?></th>
                        </tr>
                        </thead>
                        <tbody>
                        <?php foreach ($forms as $form_id => $form):
                            $url = ENTRADA_URL . "/admin/assessments/forms?section=edit-form&form_id=" . $form_id;
                            $form_objectives = "";
                            if (!empty($form["form_objectives"]) && is_array($form["form_objectives"])) {
                                $form_objectives_list = array();
                                foreach ($form["form_objectives"] as $form_objective) {
                                    $form_objectives_list[] = $form_objective["objective_code"];
                                }
                                $form_objectives = implode(", ", $form_objectives_list);
                            }
                            $objective_displayed = array();
                            $current_procedure = "";
                            if ($is_procedure_form) {
                                foreach ($form["item_objectives"] as $objective) {
                                    if ($objective["objective_code"] == "procedure") {
                                        if (!in_array($objective["objective_name"], $objective_displayed)) {
                                            $current_procedure = html_encode($objective["objective_name"]);
                                        }
                                        $objective_displayed[] = $objective["objective_name"];
                                    }
                                }
                            }
                            ?>
                            <tr>
                                <td><a target="_blank" href="<?php echo $url; ?>"><?php echo $form["title"]; ?></a></td>
                                <?php if ($is_procedure_form): ?>
                                    <td><?php echo $current_procedure; ?></td>
                                <?php endif; ?>
                                <td><?php echo $form_objectives; ?></td>
                                <td><?php echo date("Y-m-d", $form["created_date"]); ?></td>
                            </tr>
                        <?php endforeach; ?>
                        </tbody>
                    </table>
                </div>
            <?php endif; ?>
        <?php else: ?>
            <div class="alert alert-info <?php echo $is_published ? "" : "hide" ?>" id="display-published">
                <ul>
                    <li><?php echo $translate->_("You have successfully completed this template. The assessment tools based on this template will be available for assessment within the hour.") ?></li>
                </ul>
            </div>
        <?php endif; ?>
        <div id="form-information-error-msg"></div>
        <div id="form-information">
            <input type="hidden" name="form_blueprint_id" id="form-blueprint-id" class="hide" value="<?php echo html_encode($form_blueprint_id) ?>"/>
            <input type="hidden" name="epas_objectives_title" id="epas_objectives_title" class="hide" value="<?php echo html_encode($objectives_title) ?>"/>
            <div class="control-group">
                <label class="control-label" for="form-blueprint-title">
                    <?php echo $translate->_("Template Title"); ?>
                </label>
                <div class="controls">
                    <input type="text" name="form_blueprint_title" id="form-blueprint-title" class="span11 <?php echo $is_published ? " disabled" : "" ?>" value="<?php echo html_encode($blueprint_title); ?>" <?php echo $is_published ? "disabled" : "" ?>/>
                </div>
            </div>
            <div class="control-group">
                <label class="control-label" for="form-blueprint-description">
                    <?php echo $translate->_("Description"); ?>
                </label>
                <div class="controls">
                    <textarea class="span11 expandable<?php echo $is_published ? " disabled" : "" ?>" name="form_blueprint_description" id="form-blueprint-description" <?php echo $is_published ? "disabled" : "" ?>><?php echo html_encode($blueprint_description); ?></textarea>
                </div>
            </div>
            <div class="control-group space-above">
                <label class="control-label" for="form-type-id"><?php echo $translate->_("Form Type"); ?></label>
                <div class="controls">
                    <input type="hidden" name="form_type_id" id="form-type-id" class="hide" value="<?php echo html_encode($selected_form_type) ?>"/>
                    <input type="text" value="<?php echo html_encode(@$form_types[$selected_form_type]->getTitle()); ?>" disabled class="disabled">
                </div>
            </div>
            <?php if ($course_related): ?>
                <div class="control-group">
                    <label class="control-label" for="course-id"><?php echo $translate->_("Course"); ?></label>
                    <div class="controls">
                        <?php if ($is_published): ?>
                            <input type="hidden" name="course_id" id="course-id" class="hide" value="<?php echo html_encode($selected_course_id) ?>"/>
                            <?php foreach ($courses_list as $course_record):
                                if ($course_record["course_id"] == $selected_course_id): ?>
                                    <input type="text" value="<?php echo html_encode($course_record["course_name"]); ?>" disabled class="disabled">
                                <?php endif; ?>
                            <?php endforeach; ?>
                        <?php else: ?>
                            <select name="course_id" id="course-id">
                                <?php if (count($courses_list) > 1): ?>
                                    <option value="0"><?php echo sprintf($translate->_("Select a %s"), strtolower($translate->_("Course"))); ?></option>
                                <?php endif; ?>
                                <?php foreach ($courses_list as $course_record): ?>
                                    <option value="<?php echo html_encode($course_record["course_id"]); ?>" <?php echo ($course_record["course_id"] == $selected_course_id) ? "selected" : "" ?>><?php echo html_encode($course_record["course_name"]); ?></option>
                                <?php endforeach; ?>
                            </select>
                        <?php endif; ?>
                    </div>
                </div>
            <?php else: ?>
                <input type="hidden" name="course_id" value="0"/>
            <?php endif;
            // Uses the same view as the forms editor
            $audience_selector = new Views_Assessments_Forms_Controls_AudienceSelector(array("mode" => "edit"));
            $audience_selector->render(
                array(
                    "authors" => $authors,
                    "related-data-key" => "data-afbauthor-id"
                )
            );
            ?>
            <script type="text/javascript">
                jQuery(function ($) {
                    if ($("#contact-selector").audienceSelector !== typeof "undefined") {
                        $("#contact-selector").audienceSelector({
                            "filter": "#contact-type",
                            "target": "#author-list",
                            "content_target": "<?php echo $form_blueprint_id; ?>",
                            "api_url": "<?php echo ENTRADA_URL . "/admin/assessments/blueprints?section=api-blueprints"; ?>"
                        });
                    }
                });
            </script>
            <div class="control-group">
                <label class="control-label" for="blueprint-include-instructions">
                    <?php echo $translate->_("Include Instructions"); ?>
                </label>
                <div class="controls">
                    <input type="checkbox" name="blueprint_include_instructions" id="blueprint-include-instructions"<?php echo($include_instructions ? " checked" : ""); ?><?php echo $is_published ? " disabled=\"disabled\"" : ""; ?> value="1"/>
                </div>
            </div>
            <div id="blueprint-instructions-div" class="control-group<?php echo (!$include_instructions) ? " hide" : ""; ?>">
                <label class="control-label" for="form-blueprint-instruction">
                    <?php echo $translate->_("Form Instructions"); ?>
                </label>
                <div class="controls">
                    <?php if ($is_published): ?>
                        <p><?php echo $instructions ?></p>
                    <?php else: ?>
                        <textarea name="form_blueprint_instructions" id="form-blueprint-instruction"><?php echo $instructions ?></textarea>
                    <?php endif; ?>
                </div>
            </div>
            <div class="row-fluid">
                <div class="alert alert-block alert-success<?php echo $publish_class; ?>" id="display-complete">
                    <ul>
                        <li><?php echo $translate->_("This template is complete and can be published."); ?></li>
                    </ul>
                </div>
            </div>
            <div class="row-fluid">
                <button id="publish-blueprint-button" class="btn btn-success pull-right space-left<?php echo $publish_class; ?>"><?php echo $translate->_("Publish"); ?></button>
                <?php if (!$is_published): ?>
                <input id="submit-button" type="submit" class="btn btn-primary pull-right" value="<?php echo $translate->_("Save"); ?>"/>
                <?php endif; ?>
            </div>
        </div>
        <?php
    }
}
